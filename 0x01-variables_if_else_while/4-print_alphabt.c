#include <stdio.h>
/**
 * main - is the where the main function starts
 * Return: it returns void value
 */
int main(void)
{
        char ch;

        for (ch = 'a'; ch <= 'z'; ch++)
        {
                if (ch = 'e' || ch = 'q')
                {
                        continue;
                }
        putchar(ch);
        }
        putchar('\n');
        return (0);
}
